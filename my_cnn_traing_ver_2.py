# Fixed Point Quantization
def quantize_tensor(tensor, integer_bits, fractional_bits):
    """
    Quantize TensorFlow tensor to signed fixed point.
    Example:
        integer_bits = 3
        fractional_bits = 8
        => Q3.8
    """

    scale = 2 ** fractional_bits #converts tensor from decimal to integer by multiplying with 2^(no. of frac. bits)
    total_bits = 1 + integer_bits + fractional_bits
    min_val = -(2 ** (total_bits - 1)) / scale
    max_val = ((2 ** (total_bits - 1)) - 1) / scale

    tensor = tf.round(tensor * scale)
    tensor = tf.clip_by_value( tensor, min_val * scale, max_val * scale )

    tensor = tensor / scale
    return tensor


# Float -> Fixed Integer
def float_to_fixed(value, fractional_bits):
    #Converts float to fixed-point integer.
    return int(np.round(value * (2 ** fractional_bits)))

# Two's Complement Conversion
def int_to_twos_complement(value, bits):
    #Converts signed integer to binary string.
    value = int(value)
    if value < 0:
        value = (1 << bits) + value
    value &= (1 << bits) - 1

    return format(value, f"0{bits}b")


# Binary File Writer
def write_binary_file(filename, values, bits):
    #Writes iterable of integers into .mem file.
    with open(filename, "w") as f:
        for value in values:
            f.write(int_to_twos_complement(value, bits))
            f.write("\n")

