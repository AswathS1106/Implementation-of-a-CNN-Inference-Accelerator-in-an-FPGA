print("Building Hardware Emulation Library")

def prepare_image(image_tensor):
    #Converts normalized tensor into unsigned 8-bit image.
    image = np.clip(
        np.round(image_tensor.squeeze() * 255),
        0, 255 ).astype(np.uint8)
    return image

def emulate_convolution(image, kernel, bias):
    """
    image  : uint8 image
    kernel : signed integer kernel
    bias   : signed integer Q-format bias

    Returns: signed accumulator output
    """

    rows = image.shape[0] - KERNEL_SIZE + 1
    cols = image.shape[1] - KERNEL_SIZE + 1

    output = np.zeros((rows, cols), dtype=np.int32)

    for r in range(rows):
        for c in range(cols):
            window = image[ r:r+KERNEL_SIZE, c:c+KERNEL_SIZE ].astype(np.int32)
            output[r, c] = np.sum(window * kernel) + bias
    return output

def emulate_relu(feature_map):
    return np.maximum(feature_map, 0)

def emulate_pool(feature_map):
    #2x2 Max Pool
    rows = feature_map.shape[0] // POOL_SIZE
    cols = feature_map.shape[1] // POOL_SIZE

    pooled = np.zeros((rows, cols), dtype=np.int32)

    for r in range(rows):
        for c in range(cols):
            pooled[r, c] = np.max( feature_map[ r*POOL_SIZE:r*POOL_SIZE+POOL_SIZE, c*POOL_SIZE:c*POOL_SIZE+POOL_SIZE ])
    return pooled


def flatten_feature_maps(feature_maps):
    #Feature Map 0 followed by Feature Map 1
    flattened = []

    for fmap in feature_maps:
        flattened.extend(fmap.flatten())
    return np.asarray(flattened, dtype=np.int32)

def emulate_dense(features, weights, bias):
    """
    features : pooled feature vector
    weights  : reordered dense weights
    bias     : signed integer bias

    Matches neuron.v
    """

    accumulator = bias << WEIGHT_FRAC_BITS
    accumulator += np.sum(features * weights)

    return int(accumulator)


def emulate_complete_pipeline(image_tensor):
    """
    Executes complete hardware pipeline.
    Returns dictionary containing every stage.
    """

    image = prepare_image(image_tensor)

    conv_maps = []
    relu_maps = []
    pool_maps = []

    for channel in range(NUM_FILTERS):

        kernel = hardware["conv"]["weights_int"][:,:,0,channel]
        bias = hardware["conv"]["bias_int"][channel]
        conv = emulate_convolution( image, kernel, bias )

        relu = emulate_relu(conv)

        pool = emulate_pool(relu)

        conv_maps.append(conv)
        relu_maps.append(relu)
        pool_maps.append(pool)

    flattened = flatten_feature_maps(pool_maps)

    neuron_outputs = []

    for neuron in range(NUM_CLASSES):
        neuron_outputs.append(
            emulate_dense( flattened, hardware["dense"]["weights_int"][:, neuron], hardware["dense"]["bias_int"][neuron] ))

    prediction = int(np.argmax(neuron_outputs))
    return {
        "image": image,
        "conv": conv_maps,
        "relu": relu_maps,
        "pool": pool_maps,
        "flatten": flattened,
        "neurons": neuron_outputs,
        "prediction": prediction }


