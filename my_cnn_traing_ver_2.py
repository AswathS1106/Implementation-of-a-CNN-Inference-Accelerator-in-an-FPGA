# Overflow Checker
def check_overflow(values, bits, name="Value"):

    minimum = -(1 << (bits - 1))
    maximum = (1 << (bits - 1)) - 1

    values = np.asarray(values)

    if np.any(values < minimum):
        print(f"WARNING: {name} underflow detected.")

    if np.any(values > maximum):
        print(f"WARNING: {name} overflow detected.")


# Dense Weight Reordering
def reorder_dense_weights(fc_weights,
                          num_feature_maps=NUM_FILTERS,
                          feature_map_size=FEATURE_MAP_SIZE):
    """
    Reorders Dense weights so that each neuron receives
        Feature Map 0
        then
        Feature Map 1
    exactly matching neuron module

    Input from Keras
        (98,3)
    Returned:
        (98,3)
    with ordering
        FM0[49]
        FM1[49]
    """

    num_neurons = fc_weights.shape[1]

    reordered = (
        fc_weights.reshape(feature_map_size, num_feature_maps, num_neurons)
        .transpose(1, 0, 2)
        .reshape(feature_map_size * num_feature_maps, num_neurons) )

    return reordered

