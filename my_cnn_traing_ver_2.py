class ClipConstraint(tf.keras.constraints.Constraint):
    #Element-wise clip to [min_value, max_value] so quantized values stay
    #within the representable Q-format range.

    def __init__(self, min_value, max_value):
        self.min_value = min_value
        self.max_value = max_value

    def __call__(self, w):
        return tf.clip_by_value( w, clip_value_min=self.min_value,
            clip_value_max=self.max_value )

    def get_config(self):
        return {"min_value": self.min_value, "max_value": self.max_value}

weight_constraint = ClipConstraint(MIN_WEIGHT, MAX_WEIGHT)
bias_constraint = ClipConstraint(MIN_BIAS, MAX_BIAS)

class TinyCNN(tf.keras.Model):

    def __init__(self):
        super().__init__()

        self.conv = tf.keras.layers.Conv2D(
            filters=NUM_FILTERS,
            kernel_size=KERNEL_SIZE,
            strides=1,
            padding="valid",
            use_bias=True,
            kernel_constraint=weight_constraint,
            bias_constraint=bias_constraint )

        self.relu = tf.keras.layers.ReLU()

        self.pool = tf.keras.layers.MaxPooling2D(
            pool_size=POOL_SIZE,
            strides=POOL_SIZE,
            padding="valid" )

        self.flatten = tf.keras.layers.Flatten()

        self.fc = tf.keras.layers.Dense(
            NUM_CLASSES,
            use_bias=True,
            kernel_constraint=weight_constraint,
            bias_constraint=bias_constraint )

    def call(self, x):

        x = self.conv(x)
        x = self.relu(x)
        x = self.pool(x)
        x = self.flatten(x)
        x = self.fc(x)
        return x


